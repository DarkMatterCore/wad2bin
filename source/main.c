int main(int argc, char **argv)
{
    (void)argc;
    (void)argv;
    
    // Convert bytes to GF(2^m) elements and elliptic curve points
    element priv_key = {0};
    os_to_elem(ecc_private_key, priv_key);
    
    ec_point pub_key = {0};
    os_to_point(parent_public_key, &pub_key);
    
    /* HASHING */
    mpz_t hash = {0};
    mpz_init(hash);
    sha1(cert_data, sizeof(cert_data), NOT_IQUE_HASH, hash);
    
    /* SIGNING */
    element r = {0};
    element s = {0};
    ecdsa_sign(hash, priv_key, r, s);
    
    element r_sig = {0};
    element s_sig = {0};
    os_to_elem(cert_signature, r_sig);
    os_to_elem(cert_signature + 32, s_sig);
    
    printf("\nRuntime signature:\n");
    print_element(r);
    print_element(s);
    
    printf("\nCertificate signature:\n");
    print_element(r_sig);
    print_element(s_sig);
    
    /* SIGNATURE VERIFICATION */
    int result = ecdsa_verify(hash, &pub_key, r, s);
    printf("\nVerify runtime signature with public key: %d\n", result);
    
    result = ecdsa_verify(hash, &pub_key, r_sig, s_sig);
    printf("\nVerify certificate signature with public key: %d\n", result);
    
    mpz_clear(hash);
    
    return 0;
}
